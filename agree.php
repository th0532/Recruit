3항   recruit 운영진 또는 관리자를 도용 또는 사칭하여, 글을 게시하거나 쪽지를 보내어 타회원을 현혹시키는 행위.

4항   recruit 운영진 또는 관리자로 오인될 소지가 있는 아이디, 닉네임, 프로필 이미지를 사용하는 행위.

5항   recruit에 입점한 업체를 상대로 협박 또는 장난을 목적으로 게시글, 코멘트를 작성하거나 쪽지로 발송하는 행위.

6항   지역감정을 유발하거나, 특정지역, recruit내 특정 게시판을 비하하기 위한 목적으로 사용되는 어휘, 문장, 도형, 음성, 영상, 사진을 의도적으로 사용하는 행위.

7항   종교와 관련된 게시글, 코멘트를 작성하는 행위.

8항   정치와 관련된 게시글, 코멘트를 작성하는 행위.

9항   모든게시판 첫 페이지에서 게시글을 펑 처리하는 행위.

        ※ 인기글, HOT게시물에 오른 게시물이 펑 처리 될 경우 사전 통보 없이 삭제 처리될 수 있습니다.

 

9조 규칙의 준용

 

본 규칙에 명시되어있지 않은 사항은 관련 법규에 의하며, 법규에 명문화 되어 있지 않은 부분은 판례나 민법 등에 따르며, 그 후 관습법을 따릅니다. recruit의 이용규칙 외에 각 게시판의 규칙, 가이드라인, 운영원칙과 이전 공지를 통한 규칙도 제재 근거로 활용됩니다. 중복되었으나 제재 수위가 상이한 규칙은 최근 규칙을 근거로 제재가 진행됩니다.

 

또한 본 규칙의 조항은 관련 법규의 개정, 원활한 사이트 운영 등을 위하여 지속적으로 추가 수정되며, 추가·수정된 이후 시행일을 함께 별도 고지하게 됩니다.

 

 

