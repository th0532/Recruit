3항   장터 및 recruit 내 입점 업체를 이용하는 과정에서 발생한 분란에 대해서는 문의게시판에 해당 내역을 공개합니다. 이후 URL을 상대방에게 쪽지나 유선으로 전달하여, 대응하도록 합니다. 이러한 과정을 거치지 않고, 상대방이 공개사실을 확인하지 않은 상황에서 recruit 내 타 게시판 에 상대에 대한 비난이나, 확인되지 않는 내용을 공유할 경우 제재를 받게 됩니다.

4항   분란의 과정 여하를 불문하고 상대를 모욕·폄하할 목적으로 상호간에 주고 받은 쪽지를 공개한 경우 최소 1주, 최대 영구 이용정지 처리됩니다.

