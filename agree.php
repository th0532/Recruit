1항   경고를 받은 회원이 지속적으로 유사한 문제를 반복하거나, 분란을 일으킬 목적으로 하는 행동으로 판단될 경우 게시물, 코멘트 삭제와 함께 최소 1주일, 최대 영구 이용정지 처리 됩니다.

