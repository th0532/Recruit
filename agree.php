19항  기타 사회적 통념상 부적절하다고 판된되는 행위.

 

3조 저작권 관련 금지사항

 

1항    recruit 내의 게시된 정보를 영리 또는 비영리 목적으로 무단 출판, 방송, 복제, 또는 제 3자에게 제공하지 않습니다.

2항    정보통신망법 등 관련 법령에 의하여 그 전송 또는 게시가 금지되는 정보(컴퓨터 프로그램 등)를 전송하거나 게시하지 않습니다.

3항    지적재산권 및 저작권이 있는 저작물을 원작자의 허가 없이 게시 또는 배포하는 행위. 또는 이를 요청하는 행위를 하지 않습니다.

         (동영상, 사진, 음성, 텍스트, URL링크를 게시물에 포함해 작성하거나, 쪽지로 발송, 코멘트 게재하는 행위도 제재를 받습니다.)

         ※ [뉴스링크 관련] 기사의 내용을 전부 또는 일부 발췌하여 게시 및 게재하는 행위는 저작권에 위배 될 수 있습니다. 반드시 URL링크만을 올려 주셔야 합니다. 본문을 게재한 경우 무통보 삭제처리가 이루어질 수 있습니다.

 

4조 악의적인 활동 금지

 

1항    의도적, 반복적, 악의적인 목적 혹은 금전적 이득을 취할 목적으로 유언비어를 유포하는 행위.

2항    금전적 이득이나, 소기목적(특정 회원, 업체 비방, 영업적 이득 등)을 달성하기 위해 여론을 조성하여, 회원들을 현혹시키거나 반목시키는 행위.

3항    허위 사실을 바탕으로 타 사이트를 비방하거나, 타 사이트에서 생긴 불미스러운(장터, 개인간의 불화 등) 일을 허위 기재하여 비난하는 행위.

4항    자동 재생되는 동영상, 음성 등이 있음에도 제목에 표기하지 않은 행위.

5항    recruit 회원의 개인 정보를 무단으로 수집하거나 유포하는 행위.

6항    recruit과 recruit 운영진에 대한 비난과 확인되지 않은 내용 유포하고 여론을 조성하는 행위. 
         ※ 운영과 관련된 모든 내용은 문의게시판에 남겨주시기 바랍니다.

7항    recruit 입점 업체에 피해를 입힐 수 있는 게시글, 코멘트을 게시하는 행위.

 

5조 사이트 이용자 간의 기본 매너

 

1항   게시글과 코멘트, 쪽지 내에서는 타인을 지칭할 때 기본적으로 존칭을 사용 합니다. 친분이 있는 경우에도 제3자에게 공개된 게시판임을 감안하여 비속어, 욕설 등을 사용하지 않습니다.

2항   recruit 회원을 지칭할 경우 비하하거나 악의적으로 조롱할 목적으로 특정 용어를 사용하지 말아야 합니다. 올바른 지칭 방법은 해당 회원의 닉네임과 '님'을 붙여서 사용합니다. (ex. recruit님)

3항   특정 회원(또는 집단)에게 욕설(부칙 3)하거나, 인격과 신체에 대한 모욕을 주거나, 비아냥(남을 은근히 비웃는 태도로 놀림)대는 행위.

4항   남성과 여성 특정 성별을 모욕, 폄하하는 정보, 어휘, 문장, 도형, 음성, 영상은 게시하지 않습니다.

5항   recruit 내에서 활용되는 기능 중 하나인 '비추천'을 특정 회원이 마음에 들지 않는 다는 이유로 사용하거나, 보복성 의도로 사용하지 않습니다.

6항   타인의 취미 및 기호 등을 폄하하거나, 모욕하지 않습니다.

 

6조 분란 발생 시 대처 방법

 

1항   경고를 받은 회원이 지속적으로 유사한 문제를 반복하거나, 분란을 일으킬 목적으로 하는 행동으로 판단될 경우 게시물, 코멘트 삭제와 함께 최소 1주일, 최대 영구 이용정지 처리 됩니다.

2항   게시판에 개인적인 분란 내용을 노출시키거나, 특정 게시물이나 회원에 대해 제 3자의 개입을 유도하여 여론을 호도하거나 마녀 재판을 추진하는 행위는 금지합니다.
       - 저격글의 경우 게시글을 작성하기 앞서 문의게시판에 신고하여, 관련 사항에 대해 확인 및 제재가 이루어진 것을

         확인하여주시기 바랍니다.

         특정한 사안에 대한 문제 확인 후 논의는 저격글로 구분하지 않습니다. 단, 문의게시판에 신고하기에 앞서 일반 게시판에

         등록하여 저격이 이루어진 경우, 게시글 삭제는 물론 최대 영구 이용정지 처리될 수 있습니다. 이는 저격글을 악의적으로 작성

         하여, 정상적으로 활동하는 회원님께 피해를 가하는 악의적인 활동을 차단하기 위한 조치인만큼 이용에 참고하여 주시기

         바랍니다.
       - 일반 게시판에서 저격을 목적으로 특정 회원의 아이디, 닉네임, 개인정보, 쪽지 내용 및 특정 회원이 작성한 글의 링크, 코멘트

         등을 노출 한 경우 명백한 저격글로 판단하고 있습니다.

3항   장터 및 recruit 내 입점 업체를 이용하는 과정에서 발생한 분란에 대해서는 문의게시판에 해당 내역을 공개합니다. 이후 URL을 상대방에게 쪽지나 유선으로 전달하여, 대응하도록 합니다. 이러한 과정을 거치지 않고, 상대방이 공개사실을 확인하지 않은 상황에서 recruit 내 타 게시판 에 상대에 대한 비난이나, 확인되지 않는 내용을 공유할 경우 제재를 받게 됩니다.

4항   분란의 과정 여하를 불문하고 상대를 모욕·폄하할 목적으로 상호간에 주고 받은 쪽지를 공개한 경우 최소 1주, 최대 영구 이용정지 처리됩니다.

        ※ 단, 상호간 공개를 합의하였을 경우에는 제재 받지 않습니다. 또한 타 회원을 칭찬하기 위한 쪽지나, 기분 좋은 쪽지에 대해서는 상대의 허가가 없는 경우에도 공개할 수 있습니다.

        ※ 탈퇴한 회원이라고 하여도, 해당 쪽지 내용을 공개하는 것은 금지됩니다.

        - 특정한 사안에 대한 문제 확인 후 논의 과정에서의 공개는 예외로 적용될 수 있으니 자세한 내용은 문의게시판을 이용해주시기 바랍니다.

        ※ 욕설, 비아냥 등에 대해서는 직접 대응하지 마시고, 문의게시판에 신고하여 주시기 바랍니다.

 

7조 제재의 예외사항

 

1항   제재가 진행된 게시물이나, 동일한 사항에 대해 중복적으로 제재가 진행되지 않습니다.

2항   정상참작이 가능한 경우와 문의게시판 또는 사이트 하단의 메일주소를 통해 해명/소명을 하여 인정된 경우 제재를 받지 않습니다.

 

8조 그 밖에 금지사항

 

1항   동일하거나 무의미한 게시물 또는 코멘트를 반복적으로 게재(도배)하여, 다른 회원의 서비스 이용을 방해하는 행위.

        ※ 도배하였을 경우 포인트가 압수와 함께 사안에 따라 회원 자격이 상실 될 수 있습니다.

