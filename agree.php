        ※ 탈퇴한 회원이라고 하여도, 해당 쪽지 내용을 공개하는 것은 금지됩니다.

        - 특정한 사안에 대한 문제 확인 후 논의 과정에서의 공개는 예외로 적용될 수 있으니 자세한 내용은 문의게시판을 이용해주시기 바랍니다.

        ※ 욕설, 비아냥 등에 대해서는 직접 대응하지 마시고, 문의게시판에 신고하여 주시기 바랍니다.

 

